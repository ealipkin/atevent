<script type="text/javascript">
	$(window).load(function() {	
		$("a.grouped_elements").fancybox({
			maxHeight	: '600',
			prevEffect	: 'fade',
			nextEffect	: 'fade',
			nextClick	: true,
			helpers	: {
				title	: {
					type: 'inside'
				},
				thumbs	: {
					width	: 100,
					height	: 50
				}
			}
		});
		$('.mail-btn').click(function(){
			var fio = $('#fio').val();
			var phone = $('#phone').val();
			var msg = $('#msg').val();
			var flag = $('.anti-spam').prop("checked");
			if(fio=='' || phone=='' || msg=='' || flag==false){
				if(fio=='' || phone=='' || msg==''){
					$.gritter.add({
						title: 'Введены не все данные',
						text: 'Пожалуйста, заполните форму полностью',
						sticky: false, 
						time: 7000,
						class_name: 'my-class'
					});
				}
				if(flag == false){
					$.gritter.add({
						title: 'Анти-спам проверка',
						text: 'Пожалуйста, поставьте галочку под формой',
						sticky: false, 
						time: 7000,
						class_name: 'my-class'
					});
				}

				$('#gritter-notice-wrapper').click(function(){
					$.gritter.removeAll();
				});

			}
			else{
				var title, alert;
				$.ajax({
					type: "POST",
					url: "/mail.php",
					data: ({
						fio : $('#fio').val(),
						email : $('#mail').val(),
						phone : $('#phone').val(),
						msg : $('#msg').val(),
						type: 'Заказ посадочных карточек'
						}),
					success: function(msg){
						//console.log(msg);
						if(msg==1){title = 'Успех!'; alert = 'Письмо успешно отправлено'}
						else {title = 'Ошибка'; alert = 'Произошла ошибка, попробуйте ещё раз'}
						$.gritter.add({
							title: title,
							text: alert,
							sticky: false, 
							time: 7000,
							class_name: 'my-class'
						});
						$('input:checkbox').removeAttr('checked');
						$('#gritter-notice-wrapper').click(function(){
							$.gritter.removeAll();
						});
						$('#myModal').modal('hide');
					}
				});
			}
		});     
	});
</script>